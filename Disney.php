<p>Walt Disney World was first opened in 1971 to supplement Disneyland in Anaheim , California which had opened in 1955. Today Walt Disney World is the most popular holiday resort in the world with over 52 million visitors every year. Disney currently owns 25,000 acres of land but only uses about half of it for its attractions and hotels. The park is currently split up into 4 theme parks (Magic Kingdom, Hollywood Studios, Epcot and Animal Kingdom), 2 water parks ( Typhoon Lagoon and Blizzard Beach) and 27 park themed hotels (e.g. Animal Kingdome Lodge, Beach Club Resort, Star Wars Hotel). Some of the most popular rides in the theme parks include Space Mountain, Big Thunder Mountain Railroad, Expedition Everest and The Twilight Zone Tower Of Terror. At the moment Disney World are in the process of constructing a new theme park called "Star Wars-Galaxy's Edge". This park will come in at 14 acres, making it the largest park. This park will feature brand new rides such as "Millennium Falcon- Smuggler's Run" and "Star Wars: Rise of the Resistance".</p>

