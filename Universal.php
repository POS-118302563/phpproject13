<p>Universal Orlando Resort was first opened in 1990, with just one park "Universal Studios" which is still one of the main attrations in the resort currently. Universal Studios consist of 8 main areas - Hollywood, Production Central, New York, San Francisco, Diagon Alley/London, World Expo, Springfield, and Woody Woodpecker's Kidzone. The resort eventually expanded amd its second park , "Universal's Island Of Adventure", in the resort was opened 1999. Visitors begin in the "Port of Entry" and move through the various islands -  Marvel Super Hero Island, Toon Lagoon, Jurassic Park, The Wizarding World of Harry Potter. On May 25 , 2017 , Universal's newest water park "Volcano Bay" was officially opened replacing the former water park "Wet n Wild". Universal are currently in the process of constructing a new park. It is currently unclear as to what the name of this park may be but a recent trademark by Universal indicates that it may be "Fantastic Worlds". A recent parthnership between Nintendo and Universal has led to the construction of a new park in the resort named "Super Nintendo World".</p>

