 <p> Larry Bird was born on the 7th of December , 1956. Bird has been described by many as one of the greatest shooters of all time and also one of the greatest players of all time. Boston drafted Bird with their 6th pick in the NBA Draft in 1978 and he started at small forward and power forward for 13 seasons. Bird recieved the NBA's most coveted award 'Most Valuable Player' 3 times in a row (1984 ,1985, 1986). Bird was also a NBA all star 12 times , winning all star MVP in 1982. During Bird's career he has won 3 NBA championships with the Celtics , while winning finals MVP twice. He was also a member of the olympic winning USA basketball team (nicknamed 'The Dream Team') in 1992. In 1996 , Bird was voted into the Hall Of Fame , confirming his status as one of the greatest of all time. Bird served as head coach of his hometown time, the 'Indiana Pacers' from 1997-2000 and also as president before stepping down in 2017.   </p>


