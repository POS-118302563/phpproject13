<p>SeaWorld Orlando first opened in 1973 and it is currently the 11th most visited theme park in the United States, bringing in over 3.5 million tourists every year. The park is broken up into 8 sections- Port of Entry, Sea of Shallows, Sea of Legends, Sea of Ice, Sea of Delight, Sea of Mystery, Sea of Fun, Sea of Power. In total the park currently has 5 rollercoasters - Manta , Mako, Kraken , Journey To Atlantis , Antarctica : Empire of the Penguin. The park also has multiple venues for live entertainment like Shamu Stadium , Dolphin Theatre and The Sea Lion and Otter Theatre. Due to backlash from the public regarding the shows involving killer whales and dolphins, SeaWorld are being forced to shut down the theatrical shows. Thes shows have already been shut down in SeaWorld San Diego and are due to be cancelled in 2019 in Orlando. There has been plenty of controversy regarding orca whales over the years. In 2010 , during a small show , one of the orca whales , Tilikum , pulled trainer Dawn Brancheau into the water and ultimately killed her. SeaWorld are now required to keep a barrier between trainers and whales at all times. </p>

